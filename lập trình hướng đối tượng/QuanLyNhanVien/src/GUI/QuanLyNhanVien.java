    private void btCapNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btCapNhapMouseClicked
        // TODO add your handling code here:\
        int index = jTable1.getSelectedRow();
        if (evt.getClickCount()==2) {
            txtMaNV.setText(jTable1.getValueAt(index,0).toString());
            txtTenNV.setText(jTable1.getValueAt(index,1).toString());
            txtNgayVL.setText(jTable1.getValueAt(index,2).toString());
            txtHeSL.setText(jTable1.getValueAt(index,3).toString());
            txtChucVu.setText(jTable1.getValueAt(index,4).toString());
            txtPC.setText(jTable1.getValueAt(index,5).toString());
            txtSPBan.setText(jTable1.getValueAt(index,6).toString());
            
        }
    }//GEN-LAST:event_btCapNhapMouseClicked

    private void btCapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhapActionPerformed
        // TODO add your handling code here:
        int index=jTable1.getSelectedRow();
        if (index==-1) {
            JOptionPane.showMessageDialog(this,"chưa chọn");
        }else{
             NhanVien nv = ds.layDanhSachNhanVien().get(index);
             if (nv instanceof NhanVienQuanLy) {
                NhanVienQuanLy nvql = (NhanVienQuanLy) nv;
                nvql.setMaNV(txtMaNV.getText());
                nvql.setTenNV(txtTenNV.getText());
                nvql.setNgayVaoLam(chuyenNgayThanhChuoi(txtNgayVL.getText()));
                nvql.setHeSoLuong(Double.parseDouble(txtHeSL.getText()));
                nvql.setChucVu(txtChucVu.getText());
                nvql.setPhuCapChucVu(Double.parseDouble(txtPC.getText()));
              
                
            }else{
                 NhanVienBanHang nvbh= (NhanVienBanHang) nv;
                 nvbh.setMaNV(txtMaNV.getText());
                nvbh.setTenNV(txtTenNV.getText());
                nvbh.setNgayVaoLam(chuyenNgayThanhChuoi(txtNgayVL.getText()));
                nvbh.setHeSoLuong(Double.parseDouble(txtHeSL.getText()));
                nvbh.setSoSanPhamBan(Integer.parseInt(txtSPBan.getText()));
            }
             napDuLieuVaoTable();
        }
    }//GEN-LAST:event_btCapNhapActionPerformed

