    private void btCapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhapActionPerformed
        // TODO add your handling code here:
        int index=jTable1.getSelectedRow();
        if (index==-1) {
            JOptionPane.showMessageDialog(this, "Chua chon");
        } else {
            Car car=ds.layDanhSachCar().get(index);
            if (car instanceof Bus) {
                Bus bus=(Bus) car;
                bus.setBienSo(txtBienSo.getText());
                bus.setGiaGoc(Double.parseDouble(txtGiaGoc.getText()));
                bus.setNamSX(chuyenNgayThanhChuoi(txtNamSX.getText()));
                bus.setNhaSX(txtNhaSX.getText());
                bus.setSoChoNgoi(Integer.parseInt(txtSoCN.getText()));
                bus.setTuyenDuong(txtTuyenDuong.getText());
            }else{
                LuxuryCar lc =(LuxuryCar) car;
                lc.setBienSo(txtBienSo.getText());
                lc.setGiaGoc(Double.parseDouble(txtGiaGoc.getText()));
                lc.setNamSX(chuyenNgayThanhChuoi(txtNamSX.getText()));
                lc.setNhaSX(txtNhaSX.getText());
                lc.setSoChoNgoi(Integer.parseInt(txtSoCN.getText()));
                lc.setCuocVanChuyen(Double.parseDouble(txtCuocVC.getText()));
                lc.setLePhiTTDB(Double.parseDouble(txtLPTTDB.getText()));
                lc.setThue(Double.parseDouble(txtThue.getText()));
            
            }
        }
        napDuLieuVaoTable();
    }//GEN-LAST:event_btCapNhapActionPerformed

