    private void myInit() {
        setTitle("Chương trình Quản lý vé tàu ");
        setLocationRelativeTo(null);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        model=(DefaultTableModel) jTable1.getModel();
        jTable1.setModel(model);
        ds = new DanhSachKhachHang();
    }

    private Date chuyenChuoiThanhNgay(String text) {
        try {
            return  new SimpleDateFormat("dd/MM/yyyy").parse(text);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return null;
    }
    public  void themMotDongVaoTable(int stt, KhachHang kh){
        model.addRow(new Object[]{stt,kh.getSoCMND(),kh.getTenKH(),chuyenNgayThanhChuoi(kh.getNgayDi()),kh.getGaDen(),kh.getGiaVe()});
    }
    public void xoaDuLieuTrongTable(){
        for (int i = jTable1.getRowCount(); i >0; i--) {
            model.removeRow(i-1);
        }
    }
    public void napDuLieuChoTable(){
        int stt=0;
        xoaDuLieuTrongTable();
        for (KhachHang kh : ds.layDanhSachKhachHang()) {
            themMotDongVaoTable(++stt, kh);
        }
    }
    public void setBlank(){
        txtSoCMND.setText("");
        txtTenKH.setText("");
        txtNgayDi.setText("");
        txtGaDen.setText("");txtGiaVe.setText("");
        txtSoCMND.requestFocus();
    }
    public String chuyenNgayThanhChuoi(Date ngay){
        return  new SimpleDateFormat("dd/MM/yyyy").format(ngay);
    }
