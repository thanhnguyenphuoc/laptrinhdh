    public void xoaDuLieuTrongJtable() {
        for (int i = jTable1.getRowCount(); i > 0; i--) {
            model.removeRow(i - 1);
        }
    }

    public void napDuLieuVaoJtable() {
        int stt = 0;
        xoaDuLieuTrongJtable();
        for (Sach s : ds.layToanBoDanhSach()) {
            themMotDanhVaoDanhSach(++stt, s);
        }
    }

