    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        //"STT", "Ma Sach", "Ngay Nhap", "Don Gia", "So Luong", "Tinh Trang", "Thue"
        int chon = jTable1.getSelectedRow();
        if (evt.getClickCount() == 2) {
            txt_maSach.setText(jTable1.getValueAt(chon, 1).toString());
            //txt_ngayNhap.setText(jTable1.getValueAt(chon, 2).toString());
            txt_donGia.setText(jTable1.getValueAt(chon, 3).toString());
            txt_soLuong.setText(jTable1.getValueAt(chon, 4).toString());
            txt_tinhTrang.setText(jTable1.getValueAt(chon, 5).toString());
            txt_thue.setText(jTable1.getValueAt(chon, 6).toString());
            try {
                txt_ngayNhap.setText(jTable1.getValueAt(chon, 2).toString());
            } catch (Exception e) {
                txt_ngayNhap.setText("");
                //JOptionPane.showMessageDialog(this, "Nhap ngay sai");
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void B_capnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_capnhatActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Ban chua chon dong!!!");
        } else {
            Sach s = ds.danhSachs.get(index);
            if (s instanceof SachGiaoKhoa) {
                SachGiaoKhoa gk = (SachGiaoKhoa) s;
                //gk = (SachGiaoKhoa) ds.danhSachs.get(index);
                gk.setMaSach(txt_maSach.getText());
                gk.setNgayNhap(chuyenChuoiThanhNgay(txt_ngayNhap.getText()));
                gk.setSoLuong(Integer.parseInt(txt_soLuong.getText()));
                gk.setDonGia(Integer.parseInt(txt_donGia.getText()));
                gk.setTinhTrang(txt_tinhTrang.getText());
            } else {
                SachThamKhoa tk = (SachThamKhoa) s;
                //tk = (SachThamKhoa) ds.danhSachs.get(index);
                tk.setMaSach(txt_maSach.getText());
                tk.setNgayNhap(chuyenChuoiThanhNgay(txt_ngayNhap.getText()));
                tk.setSoLuong(Integer.parseInt(txt_soLuong.getText()));
                tk.setDonGia(Integer.parseInt(txt_donGia.getText()));
                tk.setThue(Double.parseDouble(txt_thue.getText()));
            }
            setBlank();
            napDuLieuVaoJtable();
        }
    }//GEN-LAST:event_B_capnhatActionPerformed

