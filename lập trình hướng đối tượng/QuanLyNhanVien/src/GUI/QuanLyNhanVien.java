    public Date chuyenNgayThanhChuoi(String str){
        try {
            return new SimpleDateFormat("dd/mm/yyyy").parse(str);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        return null;
    }
    public String chuyenChuoiThanhNgay(Date date){
        return new SimpleDateFormat("dd/mm/yyyy").format(date);
    
    }
    public void themMotDongVaoTable(NhanVien nv){
        if (nv instanceof NhanVienQuanLy) {
            NhanVienQuanLy nvql =(NhanVienQuanLy) nv;
            model.addRow(new Object[]{nvql.getMaNV(),nvql.getTenNV(),chuyenChuoiThanhNgay(nvql.getNgayVaoLam())
            ,nvql.getHeSoLuong(),nvql.getChucVu(),nvql.getPhuCapChucVu(),0,nvql.tinhLuongThucNhan()});
        }else{
            NhanVienBanHang nvbh =(NhanVienBanHang) nv;
            model.addRow(new Object[]{nvbh.getMaNV(),nvbh.getTenNV(),chuyenChuoiThanhNgay(nvbh.getNgayVaoLam()),
            nvbh.getHeSoLuong(),0,0,nvbh.getSoSanPhamBan(),nvbh.tinhLuongThucNhan()});
        }
    }
    public void xoaDuLieuTable(){
        for (int i =jTable1.getRowCount(); i >0; i--) {
            model.removeRow(i-1);
        }
    }
    public void napDuLieuVaoTable(){
        xoaDuLieuTable();
        for (NhanVien nv : ds.layDanhSachNhanVien()) {
            themMotDongVaoTable(nv);
        }
    }
