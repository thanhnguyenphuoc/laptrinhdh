    public Date chuyenNgayThanhChuoi(String str){
        try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(str);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return null;
    }
    public String chuyenChuoiThanhNgay(Date date){
        return new SimpleDateFormat("dd/MM/yyyy").format(date);
    }
    public void themMotDongVaoTable(Car c){
        if (c instanceof Bus) {
            Bus  b = (Bus) c;
            model.addRow(new Object[]{b.getBienSo(),b.getNhaSX(),b.getSoChoNgoi(),
                chuyenChuoiThanhNgay(b.getNamSX()),b.getTuyenDuong(),b.getGiaGoc(),0,0,0,b.tinhGiaXe()});
        }else{
            LuxuryCar lc = (LuxuryCar)c;
            model.addRow(new Object[]{lc.getBienSo(),lc.getNhaSX(),lc.getSoChoNgoi(),chuyenChuoiThanhNgay(lc.getNamSX())
            ,lc.getGiaGoc(),0,lc.getCuocVanChuyen(),lc.getLePhiTTDB(),lc.getThue(),lc.tinhGiaXe()});
        }
    }
    public void xoaDuLieuTrongTable(){
        for (int i = jTable1.getRowCount(); i>0; i--) {
             model.removeRow(i-1);
        }
    }
    public void napDuLieuVaoTable(){
        xoaDuLieuTrongTable();
        for (Car c : ds.layDanhSachCar()) {
            themMotDongVaoTable(c);
        }
    }
