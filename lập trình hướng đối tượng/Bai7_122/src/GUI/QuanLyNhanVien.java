    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        if (cbNhanVien.getSelectedIndex()==0) {
            NhanVienQuanLy nvql = new NhanVienQuanLy(txtChucVu.getText(),
                    Double.parseDouble(txtPCChucVu.getText()),txtMaNV.getText(),txtTenNV.getText(),
                    chuyenChuoiThanhNgay(txtNgayVL.getText()),
            Double.parseDouble(txtHeSL.getText()));
            ds.themDanhSachNhanVien(nvql);
        }else{
            NhanVienBanHang nvbh = new NhanVienBanHang(Integer.parseInt(txtSLban.getText()),
                   txtMaNV.getText(),txtTenNV.getText(),chuyenChuoiThanhNgay(txtNgayVL.getText()),
                    Double.parseDouble(txtHeSL.getText()));
            ds.themDanhSachNhanVien(nvbh);
        }
        napDuLieuTrongTable();
    }//GEN-LAST:event_btThemActionPerformed

