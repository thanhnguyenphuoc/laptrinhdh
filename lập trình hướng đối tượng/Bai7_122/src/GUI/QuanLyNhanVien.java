    public Date chuyenChuoiThanhNgay(String str){
          try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(str);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return null;
    }
    public String chuyenNgayThanhChuoi(Date date){
        return new SimpleDateFormat("dd/MM/yyyy").format(date);
    }
    
    public void themMotDongVaoTable(NhanVien nv){
        if (nv instanceof NhanVienQuanLy) {
            NhanVienQuanLy nvql = (NhanVienQuanLy) nv;
            model.addRow(new Object[]{nvql.getMaNV(),nvql.getTenNV(),
                chuyenNgayThanhChuoi(nvql.getNgayVL()),nvql.getHeSL(),nvql.getChucVu(),nvql.getPhuCapChucVu()
            ,0,nvql.tinhLuongThucNhan()});
        }else{
            NhanVienBanHang nvbh = (NhanVienBanHang) nv;
            model.addRow(new Object[]{nvbh.getMaNV(),nvbh.getTenNV(),chuyenNgayThanhChuoi(nvbh.getNgayVL()),
            nvbh.getHeSL(),0,0,nvbh.getSoSPBan(),nvbh.tinhLuongThucNhan()});
        }
        
    }
    public void xoaDuLieuTrongTable(){
        for (int i =jTable1.getRowCount(); i>0; i--) {
                model.removeRow(i-1);
        }
    }
    public  void napDuLieuTrongTable(){
        xoaDuLieuTrongTable();
        for (NhanVien nv : ds.layDanhSachNhanVien()) {
                themMotDongVaoTable(nv);
        }
    
    }
