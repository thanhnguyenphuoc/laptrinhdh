    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if (evt.getClickCount()==2) {
            txtSoCMND.setText(jTable1.getValueAt(index,1).toString());
            txtTenKH.setText(jTable1.getValueAt(index, 2).toString());
            txtNgayDi.setText(jTable1.getValueAt(index,3).toString());
            txtGaDen.setText(jTable1.getValueAt(index, 4).toString());
            txtGiaVe.setText(jTable1.getValueAt(index, 5).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();//chọn 1 dong trong jtablel
        if (index==-1) {//nếu không cọn mà bấm bt cập nhật
            JOptionPane.showMessageDialog(this,"chua chon ban ghi");
        } else {//nếu có chọn
            //truy cập tới bản ghi trong ArrayList
            KhachHang kh = ds.danhsachs.get(index);
            kh.setSoCMND(txtSoCMND.getText());
            kh.setTenKH(txtTenKH.getText());
            kh.setNgayDi(chuyenChuoiThanhNgay(txtNgayDi.getText()));
            kh.setGaDen(txtGaDen.getText());
            kh.setGiaVe(Double.parseDouble(txtGiaVe.getText()));
            napDuLieuChoTable();
            setBlank();
        }
    }//GEN-LAST:event_btCapNhatActionPerformed

