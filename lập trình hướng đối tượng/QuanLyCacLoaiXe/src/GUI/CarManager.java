    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        if (cbCar.getSelectedIndex()==0) {
            Bus b = new Bus(txtTuyenDuong.getText(),txtBienSo.getText(),txtNhaSX.getText(),Integer.parseInt(txtSoCN.getText()), 
                    chuyenNgayThanhChuoi(txtNamSX.getText()),Double.parseDouble(txtGiaGoc.getText()));
            ds.themVaoDanhSachCar(b);
        }else{
            LuxuryCar lc = new LuxuryCar(Double.parseDouble(txtCuocVC.getText()),
                    Double.parseDouble(txtLPTTDB.getText()),Double.parseDouble(txtThue.getText()),txtBienSo.getText(),
                    txtNhaSX.getText(), 
                    Integer.parseInt(txtSoCN.getText()), 
                    chuyenNgayThanhChuoi(txtNamSX.getText()),Double.parseDouble(txtGiaGoc.getText()));
            ds.themVaoDanhSachCar(lc);
        }
        napDuLieuVaoTable();
    }//GEN-LAST:event_btThemActionPerformed

