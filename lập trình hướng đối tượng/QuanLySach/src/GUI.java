    private void b_themMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_themMoiActionPerformed
        // TODO add your handling code here:
        if (cb_loaiSach.getSelectedIndex() == 0) {
            SachGiaoKhoa gk = new SachGiaoKhoa(txt_tinhTrang.getText(),
                                          txt_maSach.getText(),
                                          chuyenChuoiThanhNgay(txt_ngayNhap.getText()),
                                          Integer.parseInt(txt_donGia.getText()),
                                          Integer.parseInt(txt_soLuong.getText()));
            ds.themVaoDanhSach(gk);
        } else {
            //double thue, String maSach, Date ngayNhap, int donGia, int soLuong
            SachThamKhoa tk = new SachThamKhoa(Double.parseDouble(txt_thue.getText()),
                                          txt_maSach.getText(),
                                          chuyenChuoiThanhNgay(txt_ngayNhap.getText()),
                                          Integer.parseInt(txt_donGia.getText()),
                                          Integer.parseInt(txt_soLuong.getText()));
            ds.themVaoDanhSach(tk);
        }
        setBlank();
        napDuLieuVaoJtable();
    }//GEN-LAST:event_b_themMoiActionPerformed

